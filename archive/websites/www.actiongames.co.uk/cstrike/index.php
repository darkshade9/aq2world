<p><a name='42'></a><span class="title">CS 1.5 first impressions</span><span class="small">&nbsp;&nbsp;by Reaper at 11:14:20</span></p>
<p>Well what can I say? Not a lot of differnce at the front-end for the average CS player to worry about, threaten to leave over etc etc. It seems that for this release Valve have concentrated in upping the steaks for the cheaters. and about time too, IMHO! It now logs, kicks and bans any cheats detected on a server, so you have been warned. There has been mention that this is also logged at a central place, but is yet to be confirmed.
<br>
<br>You may find, as I have, that actions bound to the F1 &amp; F2 keys no longer work. This is because they are now reserved for the new <span style="font-weight:bold">Tracker</span> and <span style="font-weight:bold">Viewdemo</span> command. So what are they?
<br>
<br>The Tracker is very similar to pingtool and Gamespy where you can check which server your CS buddies are playing on and launch games. I have not had the chance to use this yet so am unable to offer any opinion yet.
<br>
<br>The viewdemo command is part of the new demo player. It&#39;s not clear if this is a test version or a release. This allows you view and record demos within the game.
<br>
<br>If you find you will never use these tools you can get your &#39;F&#39; keys back by going into your cstrike/cl_dlls directory and renaming the GameUI.dll file to something else.
<br>
<br>Also, for those of you with good eye sight you can now run Half-Life at 1600x1200.
<br>
<br>Finally there is a new map de_piranesi. So look forward to playing that one <img src="https://assets.aq2world.com/archive/websites/www.actiongames.co.uk/actiongames/smilies/smile.gif" height="16" width="16" alt="">
<br>
<br>On a note about CS1.6, it looks like this update will be soley distributed via the Steam method, which has been beta tested by Broadband users only as it pushes the files to you when CS is launched. It has now been streamlined to just push the required files to you and not everything, making it possible to 56kr&#39;s to use this method. We shall see just how smooth this process is when 1.6 is release late summer.
<br>
<br>Enjoy <img src="https://assets.aq2world.com/archive/websites/www.actiongames.co.uk/actiongames/smilies/smile.gif" height="16" width="16" alt="">
