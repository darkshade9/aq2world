<p><a name='76'></a><span class="title">64-bit Counter-Strike Linux Server</span><span class="small">&nbsp;&nbsp;by Fean0r at 18:12:02</span></p>
<p>Good news for server admins. Valve will be supporting AMD&#39;s upcoming 64 bit Opteron. Here&#39;s the PR:
<br>VALVE ANNOUNCES SUPPORT FOR UPCOMING AMD OPTERON(tm) PROCESSOR
<br>64-bit Counter-Strike Linux Server Provides Performance Gain for World&#39;s #1
<br>Online Action Game
<br>
<br>San Jose, Calif. - March 6, 2003 - Valve, L.L.C., creators of Counter-Strike and Half-Life, today announced immediate availability of a 64-bit version of the Counter-Strike dedicated server using the upcoming AMD OpteronTM processor. Counter-Strike has the largest service footprint of any game on the Internet, with 35,000 servers generating over 4.5 billion player minutes per month.
<br>
<br>&quot;These server operators are extremely sophisticated, and were some of the earliest adopters of Linux,&quot; said Gabe Newell, Valve managing director. &quot;We expect them to be leading-edge adopters of the AMD64 platform. AMD&#39;s approach to 64-bit computing looked great on paper, and it&#39;s nice to see that with real processors and development tools that it fulfills that promise. Every PC developer should be looking to get their server code and development tools running in 64-bits right away.&quot;
<br>
<br>In a straight port of code highly optimized for x86-32, Counter-Strike dedicated server tests with both 32- and 64-bit versions revealed a 30% clock-for-clock gain, and is expected to show further performance gains in future upgrades.
<br>
<br>&quot;Valve Counter-Strike servers with 64-bit computing can offer customers a better overall gaming experience, and AMD processors will enable this performance boost,&quot; said Barry Crume, director of server segment product marketing, Computational Products Group, AMD. &quot;With 64-bit dedicated servers using AMD Opteron processors, Valve will offer online gamers increased reliability, improved stability and greater throughput.&quot;</p>
