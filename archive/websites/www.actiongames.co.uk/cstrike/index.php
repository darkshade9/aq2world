<p><a name='77'></a><span class="title">Counter-Strike  and ethics</span><span class="small">&nbsp;&nbsp;by Fean0r at 00:00:51</span></p>
<p>I read this and thought some people might find it interesting -
<br>
<br>Introduction:
<br>
<br>This article is basically an overview of Counter-Strike and the &quot;ethics&quot; a player should strive for to help create a fun and challenging gaming environment. Each section of this article will cover a different aspect of CS ethics, from cheating to chatting.
<br>
<br>Part I: Cheating
<br>
<br>I think it&#39;s safe to say that the overwhelming majority of Counter-Strike players do not cheat. I also think it&#39;s safe to say that that same majority believes cheating to be wrong and will not condone it. I for one believe that any kind of cheating, no matter how it&#39;s used, is wrong and goes totally against the way CS was made to be played. Even though I and thousands of other CS players believe cheating to be wrong, there is no way to totally prevent all types of cheating. After all (and this may sound strange), each player has the right to cheat. They purchased their version of Half-Life and they downloaded their copy of CS. It is up to the server administrators to decide how to deal with cheaters, not the game developers.
<br>
<br>The anti-cheat movement in CS has done some very good things in the fight against cheating, but it has also created a lot of paranoid CS players who will accuse someone of cheating at the drop of a hat, so to speak. Nothing makes me angrier than doing really good on a map and having people accuse me of cheating, especially when I know I&#39;m not. I&#39;m sure many of you have had similar experiences. The point is that in its efforts to create a fair, cheat-free environment for legitimate CS players, the anti-cheat movement has also created &quot;witch hunters,&quot; who are so passionate against cheating that they are blind from the fact that some very talented CS players actually do exist. The reality of the situation is that cheating is a very simple thing to spot. Simply by watching someone through spectator mode will dispel most accusations. I have seen many players literally following an enemy&#39;s movements through walls, who I know don&#39;t cheat. It appears that they are using a wallhack, but the truth is that they are listening to the footsteps of the enemy.
<br>
<br>So&amp;#8230;next time you are about to accuse someone of cheating, please take a second look.
<br>
<br>Part II: Customization
<br>
<br>There are some CS players (myself included) who would like nothing better than to see everyone use the default skins, models, sprites, sounds, etc. to create a perfectly level playing field. However, skins (most of them) aren&#39;t a form of cheating and usually don&#39;t present any kind of advantage. Of course there are those bright blue and bright red skins for each team, but those would be considered by most people to be cheats. Remember, the very reason that Counter-Strike was able to exist is because Valve made Half-Life such an easily customizable game. Should we really take that away from the fans? Even though I would like to see everyone play with a 100% default version of Counter-Strike, I must also acknowledge that most players&#39; customizations of CS are in no way giving them an advantage.
<br>
<br>Sounds are another popularly customized aspect of CS. As with any customization, this usually will never give the player any kind of advantage over a player using the default sounds. However, (as always) there are some people who will change the silenced guns&#39; sound to be as if it had no silencer at all. That, in my opinion, is cheating, because it gives that player an unfair advantage over the majority of other CS players. However, the popular use for custom sounds is no different than applying custom skins to your game.
<br>
<br>There are so many ways to customize CS that I can&#39;t possibly go through them all and still keep this article at a reasonable length. Just remember, most people who customize the various aspects of CS do so in a non-cheating way. However, there are always those few people who choose to do so in a way that they know will be very advantageous to them. Those people I consider to be cheating.
<br>
<br>As always, though, the player who does this has every right to do so. It is, again, up to the server administrators what the consequence of misuse of customization will be, not the game developers.
<br>
<br>Part III: Chatting
<br>
<br>I have connected to so many servers which have notices of basically what you should and shouldn&#39;t say in that server, which I encourage. Having these notices will set a tone of mature chatting in your server. As far as ethics go though, there is no real way to say that one way of chatting is any more ethical than another. However offensively a player may chat, they have the right to do so, but at their own risk. That player risks being dealt with by the server administrator. So the next time you are in a server and some &quot;camper&quot; or &quot;AWP whore&quot; is beginning to make you angry, make sure you don&#39;t let your emotions get you kicked or banned from the server. Basically, watch what you say.
<br>
<br>Part IV: Gameplay
<br>
<br>The reason that CS has become such a madly popular MOD for Half-Life is based almost entirely on its gameplay. Each different map type for CS has a different style of gameplay. For example, we all know that on cs_ maps the CTs are supposed to rescue the hostages and the Ts are supposed to prevent that from happening, and that on de_ maps the Ts are supposed to bomb the bomb sites and the CTs are supposed to prevent this from happening. Of course we can&#39;t expect the brand new CS players to fully understand how each map type should be played properly. What angers me, though, is seeing veteran CS players doing nothing more than thinking about themselves and how many frags they can rack up. Too many times have I heard &quot;the bomb has been planted&quot; and see my only living teammate run and camp somewhere until it explodes. Too many times have I seen my only living teammate run and camp somewhere while the CTs rescue all the hostages. And no matter how much this angers all of us, again, these players have the right to play the game how they want to. But again, they run the risk of consequence by the server administrators. Most server administrators won&#39;t tolerate repeated behavior like that, and chances are that player will be kicked or banned for it. There are of course millions of other aspects of gameplay that so many players just decide to ignore, but it&#39;s time to end this article. In conclusion, you can play the game however you want, but remember: you do so at your own risk, because chances are a server administrator is probably watching you.</p>
