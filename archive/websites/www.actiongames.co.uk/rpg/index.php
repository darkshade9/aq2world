<p><a name='48'></a><span class="title">Yesterhaven</span><span class="small">&nbsp;&nbsp;by smurphster at 22:12:08</span></p>
<p>The third world of Ehb has been released on the CD of August&#39;s issue of <a href="http://www.pcgamer.com" target="_BLANK" class="pagelink">PC Gamer</a>. The world is named Yesterhaven and is located in the northwestern edge of the Kingdom of Ehb. The new map has new quests and creatures so if you&#39;ve been looking for something to spice up Dungeon Siege, this is your ticket.
<br>
<br>I can&#39;t seem to find anywhere to download it from. If I do, I&#39;ll be sure to post it. In the meantime, you can always buy a copy of <a href="http://www.pcgamer.com" target="_BLANK" class="pagelink">PC Gamer</a> (sorry, you brits are out of luck).
<br>
<br>The next beta patch is getting closer to release. There&#39;s a list of what the patch includes in the news on <a href="http://www.dungeonsiege.com" target="_BLANK" class="pagelink">DungeonSiege.com</a>.</p>
