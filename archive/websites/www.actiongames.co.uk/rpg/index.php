<p><a name='61'></a><span class="title">Dungeon Siege v1.1</span><span class="small">&nbsp;&nbsp;by smurphster at 22:14:08</span></p>
<p>The first official patch for Dungeon Siege was released yesterday. You can grab Dungeon Siege v1.1 from <a href="http://www.dungeonsiege.com" target="_BLANK" class="pagelink">DungeonSiege.com</a> or connect to ZoneMatch in the game and it will automatically download the update. Please be careful when downloading the patch. There are 2 versions of the patch out there. One that will update the game from v1.0 (retail) and another that will update from v1.09.2 (the second beta patch). If you downloaded the first beta but not the second, it looks like you&#39;re out of luck.</p>
