<p><a name='92'></a><span class="title">Priston Tale</span><span class="small">&nbsp;&nbsp;by smurphster at 20:02:03</span></p>
<p>If you&#39;ve stopped by the forums, I&#39;m sure you&#39;ve read a few topics about Priston Tale.  Priston Tale is a fantasy based MMORPG that is free to download and free to play.  It currently features two races which have their own starting towns.  The world is pretty large and the scenery is varied quite a bit between regions.  The same is true for monsters.
<br>
<br>The game is still in development and its difficult to see what will happen to the game post-release since the English version of the site isn&#39;t very extensive.  Enjoy the free play while it lasts!
<br>
<br>Stop by the <a href="http://eng.prisontale.com" target="_BLANK" class="pagelink">Priston Tale</a> site to see what all the buzz is about.</p>
