	<p><b>Search Rules</b></p>
	<p>This search engine helps you find documents on this website and related sites. Here's how it works: you tell the search service what you're looking for by typing in keywords, phrases, or questions in the search box. The search service responds by giving you a list of all the Web pages in our index relating to those topics. The most relevant content will appear at the top of your results.</p>
	<p><b>How To Use:</b></p>
	<ol>
		<li>Type your keywords in the search box.</li>
		<li>Press the Search button to start your search.</li>
	</ol>
	<p><b>Here's an example:</b></p>
	<ol>
		<li>Type <a href="search.pl?Terms=sky+greydeath">sky greydeath</a> in the search box.</li>
		<li>Press the Search button or press the Enter key.</li>
		<li>The Results page will show you numerous pages on the Web about skies made by GreyDeath.</li>
	</ol>
	<p>Tip: Don't worry if you find a large number of results. In fact, use more than a couple of words when searching. Even though the number of results will be large, the most relevant content will always appear at the top of the result pages.</p>
